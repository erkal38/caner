package selfStudy;

public class Araba {
  public int door;
  protected double engine;
  public String model;
  public int year;
public Araba() {
	this.door = door;
	this.engine = engine;
	this.model = model;
	this.year = year;
}
@Override
public String toString() {
	return "Araba [door=" + door + ", engine=" + engine + ", model=" + model + ", year=" + year + "]";
}
  
}
