package selfStudy;

public class PrimCast {

	public static void main(String[] args) {
      int i=(int)3.9;
      System.out.println(i);
      //Implicit casting
      //byte>short>int>long>float>double
      double d1=200;
      System.out.println(d1);
      //Explicit casting
      //reverse to implicit casting
      byte b1=5;
      short sh1=400;
      b1=(byte)sh1;
      System.out.println(b1);
    		  
      
		
	}

}
